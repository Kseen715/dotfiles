# Windows Update (wuauserv) — двойственный меч
# Что делает: Устанавливает обновления безопасности и функциональные обновления.

# Почему это опасно: Полное отключение оставит систему уязвимой. Но автоматические обновления могут устанавливаться в неудобное время (посреди игры).

# Компромисс: Установите тип запуска Manual вместо Automatic

# Как обновлять вручную: Выполняйте раз в неделю:

# # Проверка и установка обновлений
# wuauclt /detectnow
# wuauclt /updatenow

# Рекомендация: Не отключайте полностью. Используйте Manual и обновляйтесь по расписанию (например, по воскресеньям в 3:00 утра).

InvokeEcho "Set-Service -Name wuauserv -StartupType Manual"