# SysMain (Superfetch)
# Что делает: Предзагружает часто используемые приложения в RAM на основе анализа поведения. Было полезно в эпоху HDD, сейчас — анахронизм.

# Потребление: 50-200 МБ RAM, постоянный анализ использования.

# Последствия: Нулевые на SSD. На HDD может быть микроскопическое замедление запуска приложений (менее 0.5 сек). Безопасно.

InvokeEcho "Stop-Service -Name SysMain -Force"
InvokeEcho "Set-Service -Name SysMain -StartupType Disabled"